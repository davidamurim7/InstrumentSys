    public void atualizaTabela(){
        DefaultTableModel model = (DefaultTableModel) tabelaInstrumentos.getModel();
        while(model.getRowCount() > 0)
            model.removeRow(0);
        
        int k = 0;
        for(Instrumento i : instrumentoDAO.selectListaDeInstrumentos()){
            model.addRow(new String[k]);
   
            tabelaInstrumentos.setValueAt(i.getCodigo(), k, 0);
            tabelaInstrumentos.setValueAt(i.getNome(), k, 1);
            tabelaInstrumentos.setValueAt(i.getCategoria(), k, 2);
            
            tabelaInstrumentos.setValueAt(formatoData.format(i.getFabricacao()), k, 3);
            tabelaInstrumentos.setValueAt(i.getCor(), k, 4);
            tabelaInstrumentos.setValueAt(i.getMarca(), k, 5);
            tabelaInstrumentos.setValueAt(i.getPreco(), k, 6);
            tabelaInstrumentos.setValueAt(String.valueOf(i.getQuantidade()), k, 7);
            k++;
        }
    }

