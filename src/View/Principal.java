    private void buscaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_buscaKeyReleased
        tabelaInstrumentos.setSelectionBackground(new Color(204, 0, 0));
        for (int i = 0; i < tabelaInstrumentos.getRowCount(); i++) {
            tabelaInstrumentos.removeRowSelectionInterval(i, i);
            if ((!busca.getText().trim().equals("")) && (String.valueOf(tabelaInstrumentos.getValueAt(i, 0)).contains(busca.getText()) || String.valueOf(tabelaInstrumentos.getValueAt(i, 1)).contains(busca.getText()))) {
                tabelaInstrumentos.addRowSelectionInterval(i, i);
            }
        }
    }//GEN-LAST:event_buscaKeyReleased

    private void buscaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_buscaFocusLost
        busca.setText("");
        for (int i = 0; i < tabelaInstrumentos.getRowCount(); i++) {
            tabelaInstrumentos.removeRowSelectionInterval(i, i);
        }
        tabelaInstrumentos.setSelectionBackground(new Color(25, 92, 127));
        tabelaInstrumentos.setRowSelectionInterval(tabelaInstrumentos.getRowCount() - 1, tabelaInstrumentos.getRowCount() - 1);
    }//GEN-LAST:event_buscaFocusLost

